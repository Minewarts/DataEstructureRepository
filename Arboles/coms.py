def find_node_by_id(root, target_id):
    """
    Busca un nodo específico en el árbol a partir de su ID usando recursividad.
    Aprovecha la propiedad del BST: si el ID buscado es menor, busca en el subárbol izquierdo.
    Si es mayor, busca en el subárbol derecho.
    """
    if root is None:
        return None
    if root.data.id == target_id:
        return root
    elif target_id < root.data.id:
        return find_node_by_id(root.leftchild, target_id)
    else:
        return find_node_by_id(root.rightchild, target_id)

def find_successor(root, target_id):
    """
    [cite_start]Encuentra el Sucesor In-order (el menor de los más grandes)[cite: 42].
    Primero localiza el nodo. Si tiene hijo derecho, el sucesor es el nodo más a la izquierda
    de ese subárbol derecho. Si no tiene hijo derecho, el sucesor es el ancestro más cercano
    por el cual pasamos girando a la izquierda.
    """
    succ = None
    curr = root
    while curr is not None:
        if target_id < curr.data.id:
            succ = curr  # Posible sucesor
            curr = curr.leftchild
        elif target_id > curr.data.id:
            curr = curr.rightchild
        else:
            # [cite_start]Nodo encontrado. Si tiene hijo derecho, buscamos todo lo posible a la izquierda [cite: 42]
            if curr.rightchild is not None:
                temp = curr.rightchild
                while temp.leftchild is not None:
                    temp = temp.leftchild
                return temp
            break
    return succ

def find_predecessor(root, target_id):
    """
    [cite_start]Encuentra el Predecesor In-order (el mayor de los más pequeños)[cite: 42].
    Sigue la lógica inversa al sucesor: si tiene hijo izquierdo, busca el nodo más
    [cite_start]a la derecha de ese subárbol[cite: 42].
    """
    pred = None
    curr = root
    while curr is not None:
        if target_id > curr.data.id:
            pred = curr # Posible predecesor
            curr = curr.rightchild
        elif target_id < curr.data.id:
            curr = curr.leftchild
        else:
            # [cite_start]Nodo encontrado. Si tiene hijo izquierdo, buscamos todo a la derecha [cite: 42]
            if curr.leftchild is not None:
                temp = curr.leftchild
                while temp.rightchild is not None:
                    temp = temp.rightchild
                return temp
            break
    return pred

def find_expired_id(root, anio_actual):
    """
    [cite_start]Busca el ID de cualquier medicamento cuyo año de vencimiento sea menor o igual al actual[cite: 35].
    Retorna apenas encuentra uno. Es recursivo y revisa primero la raíz, luego izquierda, luego derecha.
    """
    if root is None:
        return None
    if root.data.anio_vencimiento <= anio_actual:
        return root.data.id

    left_res = find_expired_id(root.leftchild, anio_actual)
    if left_res is not None:
        return left_res

    return find_expired_id(root.rightchild, anio_actual)

def range_query_bst(root, low, high):
    """
    [cite_start]Consulta por Rango: Imprime los medicamentos que se encuentren entre dos IDs[cite: 37, 38].
    Optimiza el recorrido evitando entrar a ramas que garantizadamente están fuera del rango.
    """
    if root is None:
        return
    # Si el ID actual es mayor que el límite inferior, vale la pena buscar a la izquierda
    if root.data.id > low:
        range_query_bst(root.leftchild, low, high)

    # Si está dentro del rango, se imprime
    if low <= root.data.id <= high:
        print(root.data)

    # Si el ID actual es menor que el límite superior, vale la pena buscar a la derecha
    if root.data.id < high:
        range_query_bst(root.rightchild, low, high)


