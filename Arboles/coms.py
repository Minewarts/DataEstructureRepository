class Medicamento:
    # Constructor de la clase Medicamento. Almacena la información de cada producto.
    def __init__(self, id_med, nombre, stock, anio_vencimiento, precio):
        self.id = id_med
        self.nombre = nombre
        self.stock = stock
        self.anio_vencimiento = anio_vencimiento
        self.precio = precio

    # Sobrecarga de operadores de comparación (<, >, ==, <=, >=).
    # Esto es crucial para que las funciones de inserción y eliminación del BST
    # (que están en iniciadores.py) puedan comparar objetos Medicamento usando su ID.
    def __lt__(self, other):
        val = other.id if isinstance(other, Medicamento) else other
        return self.id < val

    def __gt__(self, other):
        val = other.id if isinstance(other, Medicamento) else other
        return self.id > val

    def __eq__(self, other):
        val = other.id if isinstance(other, Medicamento) else other
        return self.id == val

    def __le__(self, other):
        val = other.id if isinstance(other, Medicamento) else other
        return self.id <= val

    def __ge__(self, other):
        val = other.id if isinstance(other, Medicamento) else other
        return self.id >= val

    # Define cómo se imprime un medicamento en consola
    def __str__(self):
        return f"ID: {self.id} | Nombre: {self.nombre} | Stock: {self.stock} | Vence: {self.anio_vencimiento} | Precio: ${self.precio:.2f}"


# Variables Globales de Estado que se actualizan en tiempo real
CONTADOR_ALERTAS = 0
VALOR_TOTAL_INVENTARIO = 0.0
TOTAL_OPERACIONES_FUSION = 0


