def registrar_medicamento(root, id_med, nombre, stock, anio, precio):
    """
    Valida e inserta un nuevo medicamento.
    [cite_start]Aplica el descuento del 10% si el ID es par [cite: 23] y actualiza los indicadores globales.
    """
    global CONTADOR_ALERTAS, VALOR_TOTAL_INVENTARIO

    if find_node_by_id(root, id_med) is not None:
        print(f"Error: Ya existe un medicamento con el ID {id_med}.")
        return root

    # [cite_start]Regla Especial: Descuento automático a IDs pares [cite: 23]
    if id_med % 2 == 0:
        precio = precio * 0.90
        print(f"Regla Especial: ID par detectado ({id_med}). Se aplicó un 10% de descuento.")

    # Creación e inserción
    nuevo_med = Medicamento(id_med, nombre, stock, anio, precio)
    root = insert_node_bst(root, nuevo_med)

    # Actualización en tiempo real de los indicadores
    VALOR_TOTAL_INVENTARIO += precio * stock
    if stock < 5:
        CONTADOR_ALERTAS += 1

    print(f"Registrado exitosamente: {nombre} (ID: {id_med})")
    return root

def procesar_venta(root, id_venta, cantidad):
    """
    [cite_start]Resta unidades del stock de un medicamento[cite: 24].
    [cite_start]Si el stock es cero, busca y sugiere el sucesor o predecesor in-order[cite: 40, 41].
    Actualiza correctamente el valor total y el contador de alertas en tiempo real.
    """
    global CONTADOR_ALERTAS, VALOR_TOTAL_INVENTARIO

    nodo = find_node_by_id(root, id_venta)
    if nodo is None:
        print(f"Error: Medicamento con ID {id_venta} no encontrado.")
        return root

    # [cite_start]Sugerencia Automática de Sustitutos si no hay stock [cite: 39]
    if nodo.data.stock == 0:
        print(f"Stock en cero para {nodo.data.nombre} (ID {id_venta}). Buscando sustitutos...")
        succ = find_successor(root, id_venta)
        if succ:
            print(f"Sugerencia Automática (Sucesor): {succ.data.nombre} (ID: {succ.data.id})")
        else:
            pred = find_predecessor(root, id_venta)
            if pred:
                print(f"Sugerencia Automática (Predecesor): {pred.data.nombre} (ID: {pred.data.id})")
            else:
                print("No se encontraron sustitutos disponibles.")
        return root

    # Validación de cantidad a vender
    if cantidad <= 0 or cantidad > nodo.data.stock:
        print(f"Cantidad inválida o stock insuficiente. Stock actual: {nodo.data.stock}")
        return root

    # Restamos el valor y la alerta del estado 'viejo' del medicamento antes de actualizarlo
    if nodo.data.stock < 5:
        CONTADOR_ALERTAS -= 1
    VALOR_TOTAL_INVENTARIO -= nodo.data.precio * nodo.data.stock

    # Aplicamos la venta
    nodo.data.stock -= cantidad

    # Sumamos el valor y la alerta del estado 'nuevo' del medicamento actualizado
    VALOR_TOTAL_INVENTARIO += nodo.data.precio * nodo.data.stock
    if nodo.data.stock < 5:
        CONTADOR_ALERTAS += 1

    print(f"Venta procesada. {cantidad} unidades de {nodo.data.nombre} descontadas.")
    return root

def consolidar_inventario(root, id_origen, id_destino):
    """
    [cite_start]Transfiere el stock de un medicamento a otro y elimina el de origen[cite: 43, 44].
    [cite_start]Lleva el conteo de la variable global de fusiones exitosas[cite: 45].
    """
    global CONTADOR_ALERTAS, VALOR_TOTAL_INVENTARIO, TOTAL_OPERACIONES_FUSION

    nodo_origen = find_node_by_id(root, id_origen)
    nodo_destino = find_node_by_id(root, id_destino)

    if nodo_origen is None or nodo_destino is None:
        print("Error en consolidación: Uno o ambos IDs no existen.")
        return root

    stock_origen = nodo_origen.data.stock

    # Actualización del nodo destino (se le suma el stock)
    if nodo_destino.data.stock < 5:
        CONTADOR_ALERTAS -= 1
    VALOR_TOTAL_INVENTARIO -= nodo_destino.data.precio * nodo_destino.data.stock

    nodo_destino.data.stock += stock_origen

    VALOR_TOTAL_INVENTARIO += nodo_destino.data.precio * nodo_destino.data.stock
    if nodo_destino.data.stock < 5:
        CONTADOR_ALERTAS += 1

    # Actualización del nodo origen (se va a eliminar, se restan sus valores)
    if nodo_origen.data.stock < 5:
        CONTADOR_ALERTAS -= 1
    VALOR_TOTAL_INVENTARIO -= nodo_origen.data.precio * nodo_origen.data.stock

    # Eliminación física del nodo de origen
    root = delete_node_bst(root, id_origen)

    # [cite_start]Contador global solicitado en el PDF [cite: 45]
    TOTAL_OPERACIONES_FUSION += 1
    print(f"Fusión exitosa: El stock del ID {id_origen} se movió al ID {id_destino}.")
    return root

def ejecutar_limpieza_caducidad(root, anio_actual):
    """
    [cite_start]Busca recursivamente cualquier medicamento caducado y lo elimina[cite: 34, 35].
    Se ejecuta en ciclo porque al eliminar un nodo el árbol se reestructura,
    garantizando así que no quede ningún nodo viejo por fuera.
    """
    global CONTADOR_ALERTAS, VALOR_TOTAL_INVENTARIO

    eliminados = 0
    while True:
        # Buscamos un ID que esté vencido
        expired_id = find_expired_id(root, anio_actual)
        if expired_id is None:
            break # Si no encuentra más, rompe el ciclo

        nodo = find_node_by_id(root, expired_id)
        if nodo:
            # Quitamos su peso de los indicadores globales antes de destruirlo
            if nodo.data.stock < 5:
                CONTADOR_ALERTAS -= 1
            VALOR_TOTAL_INVENTARIO -= nodo.data.precio * nodo.data.stock

        # Eliminamos el nodo
        root = delete_node_bst(root, expired_id)
        eliminados += 1
        print(f"Eliminado por caducidad automática: ID {expired_id}")

    if eliminados == 0:
        print("No se encontraron medicamentos caducados.")
    return root


